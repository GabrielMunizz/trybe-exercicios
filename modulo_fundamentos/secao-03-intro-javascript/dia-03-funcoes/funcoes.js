let saldoInicial = 100;
let saldoFinal;
function adiciona(dinheiros) {
    return saldoFinal = saldoInicial + dinheiros
    
}
function subtrai(dinheiros) {
    return saldoFinal = saldoInicial - dinheiros
}
function multiplica(dinheiros) {
    return saldoFinal = saldoInicial * dinheiros
}
function divide(dinheiros) {
    return saldoFinal = saldoInicial / dinheiros
}

console.log(adiciona(299));
console.log(subtrai(50));
console.log(multiplica(5));
